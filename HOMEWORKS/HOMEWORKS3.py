# тобто вказане ім'я, прізвище та дати народження та смерті.
# Написати програму, що отримуючи такий рядок через input()
# виводить на екран окремими рядками ім'я та прізвище людини
# та її вік в роках. Для простоти при розрахунку віку можете ігнорувати
# число місяця та місяць. Тобто для рядку 'Taras Shevchenko*1814-03-09*1861-03-10' програма має вивести:

str2 = 'Taras Shevchenko*1814-03-09*1861-03-10'.replace("*", " ").replace("-", " ").split()
print(f"Name: {str2[0]} {str2[1]}")
