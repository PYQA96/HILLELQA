# Написати програму, що перетворює значення рядкової змінної з формату snake_case
# в формат CapitalizedWords (a.k.a. Capitalized camelCase). Значення змінної отримуйте з input().
# Для простоти вважаємо, що значення змінної завжди складається з 3-х слів, але, можна оброблювати і "загальний випадок",
# тобто, коли будь-яка кількість слів. Наприклад: 'employee_first_name' -> 'EmployeeFirstName'.
massstr=[str(i).capitalize() for i in input().split("_")]
print(*massstr)

