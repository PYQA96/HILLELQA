#
# виведить третій символ цього рядка.
# виведить передостанній символ цього рядка.
# виведить перші п'ять символів цього рядка.
# виведить весь рядок, крім двох останніх символів.
# виведить усі символи з парними індексами (вважаючи, що індексація починається з 0, тому символи виводяться починаючи з першого).
# виведить усі символи з непарними індексами, тобто, починаючи з другого символу рядка.
# виведить усі символи у зворотному порядку.
# виведить усі символи рядка через один у зворотному порядку, починаючи з останнього.
# виведить довжину цього рядка.
# Оформіть вивід кожного підпункту, щоб було зрозуміло, що саме виводиться. Тобто перед виводом результату треба ще вивести текст пояснення, що це.


str4 = "1234567890"
print(str4[3])  #виведить третій символ цього рядка
print(str4[-1])  # виведить передостанній символ цього рядка.
print(str4[:5])  # виведить перші п'ять символів цього рядка.
print(str4[:-2])  # виведить весь рядок, крім двох останніх символів.
print(str4[::2])  # виведить усі символи з парними індексами (вважаючи, що індексація починається з 0, тому символи виводяться починаючи з першого).
print(str4[2::3])  # виведить усі символи з непарними індексами, тобто, починаючи з другого символу рядка.
print(str4[::-1])  # виведить усі символи у зворотному порядку.
print(str4[-2::-1])  # виведить усі символи рядка через один у зворотному порядку, починаючи з останнього.
print(len(str4))  # виведить довжину цього рядка.
